											<h3>¿QUE ES BIM?</h3>
											<p>
                                                Building Information Modeling o Modelado de Información de Construcción, es un innovador sistema metodológico multidimensional en proyectos de infraestructura, debido a su eficiencia en todas las fases del proceso constructivo lo cual está suponiendo una auténtica revolución en el sector de la construcción.

                                            </p>
										</div>
									</li>
									<li>
										<div class="author-designation-comment">
											<h3>Ciclo de vida de proyecto BIM</h3>
											<p>
                                                BIM es un método multidimensional, pues contempla el desarrollo de un proyecto durante todo su ciclo de vida, desde su concepción, planificación, diseño, construcción, mantenimiento y gestión en las Dimensiones BIM (2D: Documentación, 3D: Modelo Tridimensional, 4D: Programación, 5D: Control de Costos, 6D: Sostenibilidad, 7D: Gestión de Operaciones).
                                            </p>
                                            <P>
                                                Todo este proceso utiliza como herramienta esencial la elaboración de un modelo único virtual para todas las especialidades, basado en elementos paramétricos, que permiten gestionar cambios simultáneos automáticos, logrando mejor calidad, materializando una optimización de costos y tiempo, lo cual se traduce en mayor eficiencia y productividad de los proyectos.
                                            </P>
                                            <img src="/img/blog/bp-1.jpg" alt="">
                                        </div>
                                    </li>
                                    <li>
                                        <div class="author-designation-comment">
                                            <h3>Evolución de la tecnología digital en construcción</h3>
                                            <img src="/img/blog/bp-1.jpg" alt="">
                                            <p>
                                                A diferencia del CAD que imita el proceso de los grafos y papel, BIM imita el proceso mismo de la construcción. Esta nueva forma de trabajo se fundamenta en el empleo de un modelo 3D inteligente, basado en datos y no sólo en geometría. En el modelo 3D de BIM existe vinculación en todo momento con la base de datos, ayudando así a la toma de decisiones a lo largo de todo el ciclo de vida del proyecto, desde la fase de diseño, hasta la construcción e instalación.
                                            </p>
                                        </div>
                                    </li>
                                    <li>
                                        <div class="author-designation-comment">
                                            <h3>Importancia de la metodología BIM</h3>
                                            <p>
                                                La metodología BIM nos permite centralizar toda la información del proyecto en un único modelo de información creado por todos los agentes participantes. Incorpora además: información geométrica, de tiempos, de costes, ambiental y de mantenimiento.
                                            </p>
                                            <p>
                                                La Metodología BIM optimiza este proceso, permitiendo la visualización de las instalaciones y construcciones físicas por parte de cada uno de los actores involucrados antes de la construcción del proyecto. De este modo, se pueden detectar los posibles problemas en las etapas iniciales del diseño, pudiendo modificar la geometría 3D del edificio o de las instalaciones antes de realizar la construcción, con el consecuente ahorro en la inversión de tiempo y recursos.
                                            </p>
                                        </div>
